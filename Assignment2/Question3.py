# Write a program to demonstrate data types that can be elements of a tuple.

stuple = ('My','name', 'is', 'Ram')	# sting tuple
print(stuple)

notuple = (2,4,6,8)					# number tuple
print(notuple)

floattuple = (1.1,2.2,3.3,4.4)		# float tuple
print(floattuple)

BoolTuple = (True,False,True)		# boolean tuple
print(BoolTuple)
